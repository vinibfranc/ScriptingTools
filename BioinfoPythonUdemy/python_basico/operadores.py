# -*- coding: utf-8 -*-
# Operadores maemáticos: + - * / ** %
# Operador de atribuição: =
# Operadores relacionais: == != > < >= <=

x = 3
y = 3
z = 4

print(x == y)
print(x < y)
print(x > y)

soma = x + y
print(soma == x)
print(soma >= x)
print(soma <= x)

# Operadores lógicos: and, or, not
print(x == y and x == z)
print(x == y or x == z)